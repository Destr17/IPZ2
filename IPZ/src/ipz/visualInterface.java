    public static boolean checkPass(String str,char[] ch){
        if(str.length()==ch.length){
            for(int i=0;i<ch.length;i++){
                if(str.charAt(i)!=ch[i]){
                    return false;
                }
                return true;
            }
        }else{return false;}
        return false;
    }

