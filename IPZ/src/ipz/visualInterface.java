    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        ArrayList<Person> checkP=DB.personsSelect();
        //JOptionPane.showMessageDialog(rootPane, checkP.toString());
        boolean b=false;
        int i=0;
        for(i=0; i<checkP.size();i++){
            if(checkP.get(i).getName().equals(jTextField1.getText())){
                if(checkPass(checkP.get(i).getPassword(), jPasswordField1.getPassword())){
                    b=true;
                    break;
                }
            }
        }
        if(b==true){
            if(checkP.get(i).getName().equals("admin")){
                adminS aS=new adminS();
                aS.setVisible(true);
                this.setVisible(false);
            }
            else{
                sCountry sC=new sCountry(checkP.get(i));
                sC.setVisible(true);
                this.setVisible(false);                 
            }           
        }else{JOptionPane.showMessageDialog(rootPane, "Incorrect Login or Password");}
    }//GEN-LAST:event_jButton1MouseClicked

